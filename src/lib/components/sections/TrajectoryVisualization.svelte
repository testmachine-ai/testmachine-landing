            let akw=[0.05,0.1,0.2,0.3,0.2,0.1,0.05][ak+3];
            asr+=akc[0]*akw; asg+=akc[1]*akw; asb+=akc[2]*akw;
          }
          aSmoothC.push([Math.round(asr),Math.round(asg),Math.round(asb)]);
