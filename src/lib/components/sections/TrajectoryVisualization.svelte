          ctx.lineWidth = aPasses[api].w;
          for (let i = 1; i < aProj.length; i++) {
            let prevAC = aSmoothC[Math.min(i-1, aSmoothC.length-1)];
            let curAC = aSmoothC[Math.min(i, aSmoothC.length-1)];
            let bri = api === 1 ? 60 : 0;
            let pc = [Math.min(255,prevAC[0]+bri),Math.min(255,prevAC[1]+bri),Math.min(255,prevAC[2]+bri)];
            let nc = [Math.min(255,curAC[0]+bri),Math.min(255,curAC[1]+bri),Math.min(255,curAC[2]+bri)];
            let al = brightness * aPasses[api].aM;

            let sx0: number,sy0: number,sx1: number,sy1: number;
            if (i===1) { sx0=aProj[0].sx; sy0=aProj[0].sy; }
            else { sx0=(aProj[i-2].sx+aProj[i-1].sx)*0.5; sy0=(aProj[i-2].sy+aProj[i-1].sy)*0.5; }
            if (i<aProj.length-1) { sx1=(aProj[i-1].sx+aProj[i].sx)*0.5; sy1=(aProj[i-1].sy+aProj[i].sy)*0.5; }
            else { sx1=aProj[i].sx; sy1=aProj[i].sy; }

            let grad = ctx.createLinearGradient(sx0,sy0,sx1,sy1);
            grad.addColorStop(0, 'rgba('+pc[0]+','+pc[1]+','+pc[2]+','+al+')');
            grad.addColorStop(1, 'rgba('+nc[0]+','+nc[1]+','+nc[2]+','+al+')');
            ctx.strokeStyle = grad;

            ctx.beginPath();
            ctx.moveTo(sx0, sy0);
            ctx.quadraticCurveTo(aProj[i-1].sx, aProj[i-1].sy, sx1, sy1);
            ctx.stroke();
          }
        }

        // Spark at tip
        if (aProj.length > 0) {
          let tip = aProj[aProj.length - 1];
          let sr = (3 + prog * 4) * dpr;
          if (isDark) {
            ctx.fillStyle = 'rgba(60,160,240,' + (brightness*0.08) + ')';
            ctx.beginPath(); ctx.arc(tip.sx, tip.sy, sr*4, 0, 6.2832); ctx.fill();
            ctx.fillStyle = 'rgba(150,215,255,' + (brightness*0.3) + ')';
            ctx.beginPath(); ctx.arc(tip.sx, tip.sy, sr*1.5, 0, 6.2832); ctx.fill();
          } else {
            ctx.fillStyle = 'rgba(20,80,160,' + (brightness*0.1) + ')';
            ctx.beginPath(); ctx.arc(tip.sx, tip.sy, sr*4, 0, 6.2832); ctx.fill();
            ctx.fillStyle = 'rgba(10,60,130,' + (brightness*0.35) + ')';
            ctx.beginPath(); ctx.arc(tip.sx, tip.sy, sr*1.5, 0, 6.2832); ctx.fill();
          }
          ctx.fillStyle = 'rgba(230,248,255,' + brightness + ')';
          ctx.beginPath(); ctx.arc(tip.sx, tip.sy, sr*0.5, 0, 6.2832); ctx.fill();
        }

        // Exploit region marker
        if (epIdx > allBolts.length * 0.25) {
          let ec = project(2.0, -1.2, 1.5, w, h);
          let pr = (18 + Math.sin(ts*0.003)*4) * dpr;
          ctx.strokeStyle = 'rgba(45,212,191,0.2)';
          ctx.lineWidth = 1*dpr;
          ctx.setLineDash([3*dpr,5*dpr]);
          ctx.beginPath(); ctx.arc(ec.sx, ec.sy, pr, 0, 6.2832); ctx.stroke();
          ctx.setLineDash([]);
          ctx.font = (8*dpr)+'px JetBrains Mono,monospace';
          ctx.fillStyle = 'rgba(45,212,191,0.4)';
          ctx.fillText('EXPLOIT REGION', ec.sx+pr+4*dpr, ec.sy+3*dpr);
        }

        // HUD strip updates
        let sPt = activeBolt.fns[stepIdx];
        let fn = functions[sPt] || functions[0];
        if (elEp) elEp.textContent = (epIdx+1) + '/' + TOTAL_EPS;
        if (elStep) elStep.textContent = (stepIdx+1) + '/' + activeBolt.points.length;

        // Highlight active nav stage
        let stageThresholds = [0, 60, 120, 180, 199];
        let activeStage = 0;
        for (let si = stageThresholds.length - 1; si >= 0; si--) {
          if (epIdx >= stageThresholds[si]) { activeStage = si; break; }
        }
        let navBtns = navBtnsContainer?.querySelectorAll('.traj-nav-btn');
        navBtns?.forEach(function(b: any, bi: number) {
          if (bi === activeStage) b.classList.add('traj-nav-active');
          else if (bi < activeStage) { b.classList.remove('traj-nav-active'); b.classList.add('traj-nav-done'); }
          else { b.classList.remove('traj-nav-active'); b.classList.remove('traj-nav-done'); }
        });

        // Action name
        if (trajActionCurrent && fn) {
          if (!drawLastAU || ts - drawLastAU > 120) {
            drawLastAU = ts;
            trajActionCurrent.textContent = fn.name;
            trajActionCurrent.style.color = 'rgb('+fn.color.join(',')+')';
          }
        }

        // Reward chart
        if (rewardChartCtx && rewardHistory.length > 0) {
          let rc = rewardChartCtx;
          let cdpr = window.devicePixelRatio || 1;
          let cRect = trajRewardChart.getBoundingClientRect();
          if (trajRewardChart.width !== Math.round(cRect.width * cdpr)) {
            trajRewardChart.width = Math.round(cRect.width * cdpr);
            trajRewardChart.height = Math.round(cRect.height * cdpr);
          }
          let cw = trajRewardChart.width, ch = trajRewardChart.height;
          rc.clearRect(0, 0, cw, ch);

          rc.strokeStyle = isDark ? 'rgba(255,255,255,0.06)' : 'rgba(0,0,0,0.08)';
          rc.lineWidth = 1;
          for (let gy = 0; gy < 4; gy++) {
            let yy = ch * 0.1 + (ch * 0.8) * gy / 3;
            rc.beginPath(); rc.moveTo(0, yy); rc.lineTo(cw, yy); rc.stroke();
          }

          let maxR = 0.1;
          for (let ri = 0; ri < rewardHistory.length; ri++) maxR = Math.max(maxR, rewardHistory[ri]);

          let padL = 2, padR = 2, padT = ch * 0.08, padB = ch * 0.08;
          let plotW = cw - padL - padR, plotH = ch - padT - padB;

          rc.beginPath();
          rc.moveTo(padL, padT + plotH);
          for (let ri = 0; ri < rewardHistory.length; ri++) {
            let rx = padL + (ri / (TOTAL_EPS - 1)) * plotW;
            let ry = padT + plotH - (rewardHistory[ri] / maxR) * plotH;
            rc.lineTo(rx, ry);
          }
          let lastRx = padL + ((rewardHistory.length - 1) / (TOTAL_EPS - 1)) * plotW;
          rc.lineTo(lastRx, padT + plotH);
          rc.closePath();
          rc.fillStyle = isDark ? 'rgba(45,212,191,0.08)' : 'rgba(45,212,191,0.12)';
          rc.fill();

          rc.beginPath();
          for (let ri = 0; ri < rewardHistory.length; ri++) {
            let rx = padL + (ri / (TOTAL_EPS - 1)) * plotW;
            let ry = padT + plotH - (rewardHistory[ri] / maxR) * plotH;
            ri === 0 ? rc.moveTo(rx, ry) : rc.lineTo(rx, ry);
          }
          rc.strokeStyle = 'rgba(45,212,191,0.8)';
          rc.lineWidth = 1.5 * cdpr;
          rc.stroke();

          if (rewardHistory.length > 0) {
            let lastVal = rewardHistory[rewardHistory.length - 1];
            let dotX = padL + ((rewardHistory.length - 1) / (TOTAL_EPS - 1)) * plotW;
            let dotY = padT + plotH - (lastVal / maxR) * plotH;
            rc.fillStyle = 'rgba(45,212,191,1)';
            rc.beginPath(); rc.arc(dotX, dotY, 3 * cdpr, 0, 6.2832); rc.fill();
            if (elRewardVal) elRewardVal.textContent = lastVal.toFixed(2);
          }
        }

        // Narrator
        if (!zoomPhase) {
          narratorDraw(ctx, activeStage, w, h, dpr, ts);
        }
      }

      // Narrator during zoom
      if (zoomPhase && (ts - zoomStartTime) < 3000) {
        narratorDraw(ctx, 3, w, h, dpr, ts);
      } else if (zoomPhase) {
        narratorHide();
        narratorOpacity = Math.max(0, narratorOpacity - 0.05);
      }

      requestAnimationFrame(draw);
    }

    // IntersectionObserver
    let observer = new IntersectionObserver(function(entries) {
      entries.forEach(function(entry) {
        if (entry.isIntersecting && !running) {
          running = true; animStartTime = 0; drawnTrails = []; trailBirths = []; rewardHistory = []; lastLogStep = -1;
          resizeCanvas(); requestAnimationFrame(draw);
        } else if (!entry.isIntersecting) { running = false; }
      });
    }, { threshold: 0.15 });
    observer.observe(trajectoryCanvas.parentElement!);
    window.addEventListener('resize', function() { if (running) resizeCanvas(); });

    // Nav buttons
    let navBtns = navBtnsContainer?.querySelectorAll('.traj-nav-btn');
    navBtns?.forEach(function(btn: any) {
      btn.addEventListener('click', function() {
        let targetEp = parseInt(btn.getAttribute('data-traj-ep'));
        if (trajExploitOverlay) trajExploitOverlay.classList.remove('traj-exploit-visible');

        drawnTrails = [];
        trailBirths = [];
        rewardHistory = [];
        let now = performance.now();
        for (let i = 0; i < targetEp; i++) {
          drawnTrails.push(allBolts[i]);
          rewardHistory.push(allBolts[i].reward);
          let age = (targetEp - i) * 30;
          trailBirths.push(now - age);
        }

        let t = 0;
        for (let i = 0; i < targetEp; i++) {
          t += allBolts[i].points.length * STEP_MS + EP_GAP_MS;
        }
        animStartTime = now - t;
        lastLogStep = -1;

        navBtns?.forEach(function(b: any) { b.classList.remove('traj-nav-active'); });
        btn.classList.add('traj-nav-active');

        if (!running) {
          running = true;
          resizeCanvas();
          requestAnimationFrame(draw);
        }
      });
    });

    // Cleanup
    return () => {
      running = false;
      observer.disconnect();
    };
  });
</script>

