            gb.addColorStop(0, 'rgba('+cL[0]+','+cL[1]+','+cL[2]+','+(energy * extPasses[epi].af)+')');
            gb.addColorStop(1, 'rgba('+cL[0]+','+cL[1]+','+cL[2]+',0)');
            ctx.strokeStyle = gb;
